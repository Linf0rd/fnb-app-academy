# Lesson 6: Operators

# Example 1


# Addition (+)

x = 10
y = 2

print("Addition:", x + y)  # Output: 12


# Subtraction (-)

print("Subtraction:", x - y)  # Output: 8


# Multiplication (*)

print("Multiplication:", x * y)  # Output: 20


# Division (/)

print("Division:", x / y)  # Output: 5.0


# Modulus (%)

print("Modulus:", x % y)  # Output: 0


# Exponentiation (**)

print("Exponentiation:", x ** y)  # Output: 100



# Example 2

x = 10
# x = x + 2  # Increment x by 2
x += 2  # Increment x by 2

print("Incremented x:", x)  # Output: 12


# Example 3

x = 10
x -= 2  # Decrement x by 2
print("Decremented x:", x)  # Output: 8