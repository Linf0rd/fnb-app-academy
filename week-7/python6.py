# Lesson 10: Loops

# Example 1 - For Loop

fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print(fruit)  # Output: apple, banana, cherry (each on a new line)



# Example 2

numbers = [1, 2, 3, 4, 7]

for number in numbers:
    print(numbers) # Output: [1, 2, 3, 4, 7] (the entire list printed each time)


# Example 3

for number in numbers:
    print(number)  # Output: 1, 2, 3, 4, 7 (each number printed on a new line)


# Example 4 - While Loop

count = 1

while count <= 5:
    print(count)  # Output: 1, 2, 3, 4, 5 (each number printed on a new line)
    count += 1  # Increment count by 1


