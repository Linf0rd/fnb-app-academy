# Lesson 3 - Modules and Builtin Functions

import math
import random
import datetime
import os
import sys


print(math.pi)  # Accessing the value of pi from the math module
print(random.randint(1, 100))  # Generating a random integer between 1 and 100
print(datetime.datetime.now())  # Getting the current date and time
print(os.getcwd())  # Getting the current working directory
print(sys.version)  # Getting the Python version
